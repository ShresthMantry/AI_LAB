a = int(input("Enter any number"))
b = int(input("Enter any number"))
ch = input("Enter operator")
if ch=='+':
        result = a+b
        print(result)
elif ch=='-':
        result = a-b
        print(result)
elif ch=='*':
        result = a*b
        print(result)
elif ch=='/':
        result = a/b
        print(result)